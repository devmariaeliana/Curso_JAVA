package Variaveis;

public class Operadores {
  
//incrementar e decrementar
  
public static void main(String args []){

  int numero = 5;
    
  numero += 12;

  System.out.println(numero);

  int x = 20;

  x = ++x +5;

  System.out.println(x);

  int y = 20;

  y = y++ +5;

  System.out.println(y);

}

}



